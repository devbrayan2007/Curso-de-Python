# ESTRUTURAS DE REPETIÇÃO - OPERADORES DE ATRIBUIÇÃO (PARTE 3)


contador = 0

#  +=
contador += 1
print(contador)


# -=
contador -= 1
print(contador)


# *= 
contador *= 1
print(contador)

# /=
contador /= 1
print(contador)

# //=
contador //= 1
print(contador)

# %=
contador %= 1
print(contador)

# **=
contador **= 1
print(contador)